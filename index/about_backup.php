<div class="para">
<h4><b>【每周体能训练】</b></h4>
<p class="mypara">
每周一三五晚20:45，北大车协组织协会会员在五四体育场进行日常体能训练。训练内容包括耐力跑，俯卧撑，靠墙马步等，参加训练提升了同学们体能，锻炼了大家的意志，也让协会会员彼此间有了进一步交流接触的机会。
</p>

<h4 style="margin-top:24pt"><b>【节假日骑行拉练】</b></h4>
<p>
骑行拉练可谓我们活动的精华。每逢周末和节假日，我们便会组织规模不等的骑行拉练活动，短则数十公里，长可达到三百公里（双日拉练），目的地多为京郊名胜。每次拉练的参与者少则五十人，多则近一百多人。
</p><p>
骑行拉练既为大家提供节假日外出的机会，也是会员了解协会、彼此熟悉的过程。队员们不仅能积累骑行经验，体验骑行乐趣，并且能为参加暑期活动做好充分的准备。作为车协开向全校同学的一个窗口，拉练也欢迎外校同学参加。
</p>

<h4 style="margin-top:24pt"><b>【义务修车】</b></h4><p>
每周五下午的北大三角地（现位于农园西北角）都有一群忙碌的身影，那就是的我们的义务修车活动。它已经有十年历史，是我们为方便全校广大师生进行的。它体现了车协立足于校园，扎根于学生之中的特点。同时，这项活动也培养同学的动手能力，提供了一种实践方式。因此，自开展以来便受到了全校师生的广泛欢迎与一致好评。
</p>
