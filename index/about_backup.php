<h3 class="text-center" style="margin-bottom:20pt"><b>暑期远征简介</b></h3>
<p>
暑期远征是北大车协一年活动中最核心的部分，每年我们会严格筛选出一批<b>三十人左右</b>的精英会员组成<b>2-3支团队</b>，由有长途骑行经验的（参加过之前的暑期远征）资深会员担任队长，带领这批精英会员<b>在暑假期间</b>（7月初至8月初）进行<b>为期一个月、长达两千公里左右</b>的远征骑行。“<span style="color:red">深入社会，融于自然；挑战极限，超越自我</span>”是协会的口号，也是暑期骑行目的的集中体现。
</p>

<h4 style="margin-top:22pt"><b>“深入社会，融于自然”</b></h4>
<p>
<img id="summer_img_1" style="width:30%;float:right">
身为北大学子，我们不仅将自行车作为兴趣爱好，更将其视作深入社会、洞察社会的有力载体。我们将于暑期途中考察各地风土民情，关注农村地区民生发展，倡导环保健康的生活方式，并且选择沿途特定地区进行<b>为期一周左右</b>的社会实践（支教、访谈等），如2011年暑期远征实践团在国家一级贫困县四川省苍溪县分小组进行初等教育、农村医疗以及农村生产经营模式的社会考察，形成考察报告，得到当地团委的认可和赞许。 
</p>
<p>
回溯历史，我们的社会考察成果颇丰。例如：</p>
<b><p>2002年的考察报告《行丽江》被国家图书馆永久珍藏。 </p>
<p>
2004年的南部水资源自行车考察荣获国家建设部大学生水情调查大奖。</p>
<p>
……</p></b>
<img id="summer_img_2" style="width:30%;float:right">

<h4 style="margin-top:22pt"><b>“挑战极限，超越自我”</b></h4>
<p>
暑期远征活动已开展十九年，总行程约八万公里，南至三亚，北至漠河，西至敦煌，东至滨海。每年的暑期，炙热的阳光之下，瓢泼的大雨之中，空气稀薄的高原之上，都有一批北大车协人奋力前行。狂风暴雨，大坡高山，恶劣的自然环境，见证了北大车协人非凡的胆魄，绝不退缩的坚韧！
</p>

<h4 style="margin-top:22pt"><b>“车轮不止，远征不息”</b></h4>
<p>
2013年，一条西北苍茫之路吸引了一批不畏艰险的骑士，“内蒙-兰州-祁连-嘉峪关”是线路的地标，但是这四个词根本不足以描述这段征程。行者们在内蒙古草原之上高歌，然后顺势西行，寻找黄河之源，紧接着遇见有油菜花盛开的青海，青海湖波色摇曳，令人陶醉。再而是祁连山脉，依山脊而上，跨过海拔四千米之上的垭口，最后屹立于嘉峪关头，纵览塞北风光。
</p>
<p>
2014年，从苍茫的大西北又回到山清水秀的西南，“张家界-凯里-昆明-西双版纳”，这是一条自然风光之途，途径黄果树瀑布、凤凰古镇、普洱县等，这更是一条与少数民族相知之旅，一路上经过各种少数民族的自治县、自治州，深刻体会西南的水土人情。在路上，行者们经历了超强台风“威马逊”，当汽车、摩托都艰难上路时，他们穿过及膝的水滩，咬牙坚持，从不言弃，这为这段温润美好的西南之旅又增添了一份凛冽的色彩。
</p>
