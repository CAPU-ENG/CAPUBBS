<h2><b>北京大学自行车协会简介</b></h2>
<p>
北京大学自行车协会，简称北大车协（Cycling Association of Peking University），是北京大学三大社团之一，于1995年10月25日成立。北大车协是以自行车运动和社会实践为活动内容，结合体育运动和学术实践的综合性学生社团，也是中国起步最早、影响力最大的大学生自行车运动的民间群众组织之一。它接受北京大学校团委的领导，并挂靠于国家体育总局中国自行车运动协会。
</p>
<p>
北大协会的日常活动包括训练、拉练、义务修车、学术讲座、文化周、高校山地车交流赛等。在活动过程中，北大车协旗下聚集了大批自行车爱好者，也与众多知名企业建立了良好的合作关系，打下了广泛而坚实的人际基础。
</p>
<p>
经过近20年的不断摸索改进，北大车协已经建立起一套成熟有效的组织领导体系。它由理事会和执委会组成，前者属权力和决策机构，由7-10名理事构成，负责监督和协助执委会工作，组织协会工作，培养协会人才，引领协会发展；后者属执行机构，其中除主席团三人之外，还下属组织部、实践部、文体部、外联部、宣传部、队医组、社考部六个部门。大家各司其职，又互相协作，使车协成为一个完善而有效的工作集体。
</p>
<p>
自成立以来，北大车协围绕自行车运动，结合社会关注的问题，成功举办了多次大型活动，拥有良好的公众反映和社会影响，北大车协年年被评为北大十佳社团，如今已是北大最具影响力的三大社团之一，同时也成为了中国高校学生社团中的佼佼者，2006年被评为“全国高校优秀学生社团标兵”。在它的带动下，全国各地众多高校纷纷成立了民间自行车组织，使得自行车运动在高校间逐渐蓬勃发展起来，越来越得到广大师生的喜爱。
</p>
<p>
本着“<b>深入社会，融于自然；挑战极限，超越自我</b>”的精神，北大车协结合大学生特点，通过体育赛事、旅行探险、社会考察、参观游览、社会服务等多种社会实践方式，围绕自行车运动，成功地举办了多次大型活动。
</p>
<p>
<img id="about_img" style="float:right;height:200px">
在骑行活动方面，自1995年北大车协创始人康小明组织的第一次暑期远征以来，我们共举办了十七次暑期远征骑行考察，总行程近十四万里，我们的行迹遍布全国，其中包括97年为庆祝香港回归的远征“北京——深圳”，2001年挑战青藏高原西部远征考察“北京——西宁——拉萨——珠峰”等路线，沿途吸引了大批媒体的报道，引来各界人士的对自行车运动的关注，对北大车协的关注，这一活动也在各高校车协中产生了影响，引起他们对单车长途远征的兴趣。随着骑行活动的有效组织和发展，北大车协已日益成为人们关注的对象和社会关注的焦点。
</p>
<p>
在竞赛竞技方面，除了参加了亚洲山地车锦标赛，首届首都高校山地车赛等许多赛事外，还于98年4月26日成功主办了八达岭－北京大学自行车拉力赛。2004年10月又成功协办北京长城自行车嘉年华。完全由我们组织筹办的全国高校山地车交流赛已成功举办多届，初成规模。
</p>
<p>
此外，北大车协的日常活动也多姿多彩，每周有三次体能训练与一次面向全校师生的义务修车，周末、节庆时有集体骑行拉练，还有各种趣味比赛。今后，车协还将努力联合其他院校，继续发展高校间自行车运动的交流与合作。
</p>

