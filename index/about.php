<h2><b>日常活动简介</b></h2>
<h3 style="margin-bottom:30px"><b>一、基础活动——锻炼身体、强健体魄</b></h3>
    <h4><b>【日常训练】</b></h4>
    <p>
        北大车协的基础活动之一，就是每周一、三、五晚8:45的日常体能训练，地点就在五四体育场。卸下一天的课业压力，和我们一起到操场上挥洒汗水，纵情呼喊吧！
    </p>
    <p style="font-size: 12pt">
    <ul style="margin-left:0%; font-size: 13pt">
        <li><b>场地训练</b>：马步、缓慢蹲起、俯卧撑、折返跑、冲刺跑、仰卧举腿等。目的在于锻炼全身肌肉力量，塑造良好身材。特别注重对于骑行非常重要的下肢力量。女生们不用担心锻炼下肢力量会造成大腿粗壮，真正的自行车运动员腿型都是修长而健美的！</li>
        <li><b>趣味训练</b>：马步胯下传球、跳台阶等综合项目。目的在于减少体能训练带来的枯燥和乏味。在团队合作和竞争当中，协会将训练项目编排进趣味活动当中，大家肩并肩，背靠背，结识新朋友，这是最好的机会！</li>
        <li><b>拓展训练</b>：信任背摔、集体游戏等。目的在于培养团队意识，让参与训练的会员们能够更加近距离接触。你会发现，在车协，交个朋友，是个很简单的事情！</li>
    </ul>
    </p>
    <p>
        特别要说的是，参加训练是完全自愿的，节奏、次数、强度完全由你把控。协会的日常活动从来没有任何门槛。唯一的门槛，是你自己愿不愿意走到我们中间来！
    </p>
    <h4 style="margin-top:24pt"><b>【节假日骑行拉练】</b></h4>
    <p>
        周一至周五穿梭在教学楼、图书馆、食堂、寝室之间，时间久了不免让人厌烦。我们带你走出燕园，去看看美丽的北京城！
    </p>
    <p>
        骑行拉练的目的地，都是京郊的风景名胜。比如卢沟桥、莲石湖、凤凰岭、石花洞、八达岭、延庆百里山水画廊等等。可能你已经在宿舍楼内的布告栏里看到，一些旅行社推出的一日游活动，但是你可能难以相信，不用花费高昂的费用，一辆单车，一天的时光，我们的旅行绝对更加精彩纷呈！
    </p>
    <p>
        此外，我们还特别举办双日拉练，会员们将一起在校园外度过美好的夜晚。篝火晚会、野炊、观日出、爬长城……这些活动，其他地方可是办不到的哦！
    </p>
    <p>
        可能你还有很多顾虑，其实我们非常知道你的想法，18年来的活动经验，我们非常了解会员需要什么。首先，不用担心安全问题，因为和几十个上百个会员一起出门，又有协会富有经验的老会员领队，保证让你开心归来。也不用担心装备车辆问题。协会老会员非常愿意把自己闲置的爱车借给你，因为他们也是像你们一样一路走来，希望让更多的人能够毫无顾虑的加入到车协当中。（详情请看“自助借车”板块）
    </p>

    <h3><b>二、部门活动——分工合作、真心奉献</b></h3>
    <p>
        除了训练和拉练，协会拥有七大部门，还有北大车协车队。这些部门都有自己的特色活动。想更深入的了解北大车协，最好各个部门活动都走一走，你会发现，协会绝对不是拿会员当“搬砖工”，而是希望会员能够在每一个部门活动都能有所收获！
    </p>
    <p>
        各个部门介绍详见“部门简介”、车队介绍详见“车队简介”。
    </p>

    <h3><b>三、特色活动——扎根校园、服务师生</b></h3>
    <p>
        北大车协作为全校“三大社”，多年蝉联“品牌社团”，除了丰富多彩的活动以外，我们还主动回馈校园，服务师生，这就是我们的“义务修车”活动。
