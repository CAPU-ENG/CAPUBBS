						拉练中请注意保护车辆。<b>不暴力换挡</b>（不要在发力蹬踏的同时使用变速器，以免损坏链条），<b>用合适的档位骑行</b>（平路不要用爬坡的小档，如1-1,1-2档，爬坡不要用平路的大档，如3-6、3-7、3-8）。注意保护车上细小零件。
