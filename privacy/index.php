<h1>CAPUBBS - 隐私保护政策</h1>
<p>我们将收集用户的如下信息：</p>
<ul class="indent">
<li>在注册时，会让用户填写邮箱，qq，来自哪个城市，爱好等个人信息；这些信息均为可选项，用户自愿填写，填写后可以让别人更好的了解自己。</li>
<li>在登录时，会向服务器发送登录的用户名和密码。</li>
<ul><li>用户可以选择是否自动登录，如果是，则会在手机本地保存用户名和密码。</li>
<li>密码将会经过加密后通过post请求发送；明文密码对任何人都不可见。</li></ul>
<li>用户登录时还会向服务器发送用户的部分手机信息。</li>
<ul><li>这里的手机信息仅包含如下几点：</li>
	<ul><li>手机型号： iPhone4(s)/iPhone5(s)/iPad/......</li>
	    <li>系统平台： IOS6/IOS7/IOS8/...</li>
	</ul>
<li>不会向向服务器发送更多的有关用户的个人信息。</li>
</ul>
<li>当且仅当用户需要进行上传图片的操作时，将会请求对用户照片库的访问，并将在取得用户选择的图片后直接进行上传操作。</li>
</ul>
<p>除上述列出的以外，将不会再收集，保存或者发送任何用户的个人信息和隐私。</p>
